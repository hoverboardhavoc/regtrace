"""Snippet build pipeline: vector YAML → ELF.

Steps:
  1. Render the implementation body into a single C translation unit, wrapping
     it in `regtrace_test`.
  2. Compile with arm-none-eabi-gcc against the vendored startup + linker
     script for the (library, target).
  3. Link against the cached HAL static library.
  4. Write the ELF to build/<library>/<rev>/<target>/<vector_id>.elf.
"""

from __future__ import annotations

import shutil
import subprocess
import tempfile
from dataclasses import dataclass
from pathlib import Path

from .. import vectors as vectors_mod
from ..paths import REPO_ROOT, build_assets_dir, build_dir
from . import hal


# v0.1 default compile flags per target. Pinned so reproductions are byte-for-byte.
TARGET_FLAGS: dict[str, tuple[str, ...]] = {
    "stm32f0": ("-Os", "-mcpu=cortex-m0", "-mthumb", "-fno-common", "-ffreestanding",
                "-fno-builtin", "-Wall"),
    "stm32f1": ("-Os", "-mcpu=cortex-m3", "-mthumb", "-fno-common", "-ffreestanding",
                "-fno-builtin", "-Wall"),
    # STM32F4 default libopencm3 build uses hard-float ABI; the snippet must
    # match or ld emits "uses VFP register arguments" mismatch errors.
    "stm32f4": ("-Os", "-mcpu=cortex-m4", "-mthumb", "-mfloat-abi=hard",
                "-mfpu=fpv4-sp-d16", "-fno-common", "-ffreestanding",
                "-fno-builtin", "-Wall"),
    "gd32f1x0": ("-Os", "-mcpu=cortex-m3", "-mthumb", "-fno-common", "-ffreestanding",
                 "-fno-builtin", "-Wall"),
    "gd32f10x": ("-Os", "-mcpu=cortex-m3", "-mthumb", "-fno-common", "-ffreestanding",
                 "-fno-builtin", "-Wall"),
}


# When a HAL needs warnings demoted (vendor SPL has many) or a specific
# C dialect (vendor SPL typedefs `bool` and won't compile under C23+ where
# `bool` is a keyword), apply per-library overrides.
LIBRARY_EXTRA_FLAGS: dict[str, tuple[str, ...]] = {
    "gd-spl":         ("-std=gnu11",
                       "-Wno-unused-but-set-variable", "-Wno-unused-variable",
                       "-Wno-comment", "-Wno-implicit-function-declaration"),
    "gd-spl-patched": ("-std=gnu11",
                       "-Wno-unused-but-set-variable", "-Wno-unused-variable",
                       "-Wno-comment", "-Wno-implicit-function-declaration"),
    # Cube LL: same C23-vs-bool issue, plus its headers warn liberally.
    "cube-ll":        ("-std=gnu11",
                       "-Wno-unused-but-set-variable", "-Wno-unused-variable",
                       "-Wno-comment", "-Wno-pointer-sign"),
}


@dataclass(frozen=True)
class BuildResult:
    elf_path: Path
    library: str
    rev: str
    target: str
    vector_id: str
    compile_flags: tuple[str, ...]
    gcc_version: str


def render_source(impl: vectors_mod.Implementation) -> str:
    """Render a translation unit containing `regtrace_test`."""
    includes = "\n".join(f"#include <{h}>" for h in impl.includes)
    return f"""\
/* Generated by regtrace — do not edit. */
{includes}

__attribute__((noinline, used))
void regtrace_test(void)
{{
{impl.body.rstrip()}
}}
"""


def auto_detect_rev(library: str) -> str:
    """Resolve `<library>`'s rev via git describe on its underlying repo."""
    return hal.auto_detect_repo_rev(library)


def build_one(vec: vectors_mod.Vector, slug: str, rev: str | None = None) -> BuildResult:
    """Compile vec.implementations[slug] into an ELF. Returns BuildResult."""
    impl = vec.implementations[slug]
    library, target = impl.library, impl.target
    flags = TARGET_FLAGS.get(target)
    if flags is None:
        raise NotImplementedError(f"no compile flags pinned for target {target!r}")

    actual_rev = rev or auto_detect_rev(library)
    assets_dir = build_assets_dir(library, target)
    if not assets_dir.exists():
        raise RuntimeError(
            f"missing build_assets/{library}/{target}/ — vendor startup.S + link.ld there"
        )

    library_flags = flags + LIBRARY_EXTRA_FLAGS.get(library, ())
    lib_a = hal.build_hal(library, target, actual_rev, library_flags)
    out_dir = build_dir(library, actual_rev, target)
    out_dir.mkdir(parents=True, exist_ok=True)
    elf_path = out_dir / f"{vec.vector_id}.elf"

    src = render_source(impl)
    with tempfile.TemporaryDirectory(prefix="regtrace-build-") as tmpdir:
        tmp = Path(tmpdir)
        snippet_c = tmp / "snippet.c"
        snippet_c.write_text(src)
        startup_s = assets_dir / "startup.S"
        link_ld = assets_dir / "link.ld"
        if not startup_s.exists() or not link_ld.exists():
            raise RuntimeError(
                f"build_assets/{library}/{target}/ must contain startup.S + link.ld"
            )

        # Library include paths + chip define. Resolve to the worktree path
        # so multi-rev captures use the right source tree.
        include_dirs: list[str] = []
        extra_defines: list[str] = []
        if library == "libopencm3":
            tree = hal.worktree_for("libopencm3", actual_rev)
            include_dirs.append(str(tree / "include"))
            target_define = {
                "stm32f0": "STM32F0",
                "stm32f1": "STM32F1",
                "stm32f4": "STM32F4",
                "gd32f1x0": "GD32F1X0",
                "gd32f10x": "GD32F10X",
            }[target]
            extra_defines.append(f"-D{target_define}")
        elif library in {"gd-spl", "gd-spl-patched"}:
            tree = hal.worktree_for(library, actual_rev)
            layout = hal.GD_SPL_LAYOUT[target]
            for d in layout.get("build_assets_includes", []):
                include_dirs.append(str(assets_dir / d))
            for d in layout["include_dirs"]:
                include_dirs.append(str(tree / d))
            extra_defines.append(f"-D{layout['chip_define']}")
            extra_defines.append("-DUSE_STDPERIPH_DRIVER")
        elif library == "cube-ll":
            tree = hal.worktree_for("cube-ll", actual_rev)
            layout = hal.CUBE_LL_LAYOUT[target]
            for d in layout["include_dirs"]:
                include_dirs.append(str(tree / d))
            extra_defines.append(f"-D{layout['chip_define']}")
            extra_defines.append("-DUSE_FULL_LL_DRIVER")

        cmd = [
            "arm-none-eabi-gcc",
            *library_flags,
            *extra_defines,
            *(f"-I{d}" for d in include_dirs),
            "-nostartfiles",
            "-T", str(link_ld),
            str(startup_s),
            str(snippet_c),
            str(lib_a),
            "-o", str(elf_path),
            "-Wl,--gc-sections",
        ]
        print("[build] " + " ".join(cmd))
        subprocess.check_call(cmd)

    return BuildResult(
        elf_path=elf_path,
        library=library,
        rev=actual_rev,
        target=target,
        vector_id=vec.vector_id,
        compile_flags=flags,
        gcc_version=hal.gcc_version(),
    )


def build_path(path: Path) -> int:
    """`regtrace build PATH` entry point."""
    try:
        vecs = vectors_mod.discover(path)
    except (FileNotFoundError, ValueError) as e:
        print(f"[error] {e}")
        return 1
    if not vecs:
        print(f"[error] no vector YAML found under {path}")
        return 1

    rc = 0
    for vec in vecs:
        for slug in vec.implementations:
            try:
                result = build_one(vec, slug)
                size = result.elf_path.stat().st_size
                print(f"[ok] {slug} → {result.elf_path}  (size: {size}B)")
            except Exception as e:
                print(f"[fail] {vec.vector_id} {slug}: {e}")
                rc = 1
    return rc
